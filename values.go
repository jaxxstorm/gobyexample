package main

import "fmt"

// creates a nice function
func main() {

  fmt.Println("go" + "lang") // concat strings with +
  fmt.Println("1+1 =", + 1+1) // added a number to a string!
  fmt.Println("7.0/3.0 =", 7.0/3.0) // working on floats

  fmt.Println(true && false) // and
  fmt.Println(true || false) // or
  fmt.Println(!true) //not

}
